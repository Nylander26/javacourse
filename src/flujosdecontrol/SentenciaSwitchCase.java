package flujosdecontrol;

public class SentenciaSwitchCase {
    public static void main(String[] args) {

        byte num = 3;
        switch (num) {
            case 0:
                System.out.println("num = " + num);
                break;
            case 1:
                System.out.println("num = " + num);
                break;
            case 2:
                System.out.println("num = " + num);
                break;
            case 3:
                System.out.println("num = " + num);
                break;
            default:
                System.out.println("no existe");
        }
    }
}
